function applyLang(lang) {
  currentLang = lang;
  document.documentElement.lang = lang;
  document.documentElement.dir = (lang === 'ar') ? 'rtl' : 'ltr';
  // data-t (text content)
  document.querySelectorAll('[data-t]').forEach(el => {
    const k = el.getAttribute('data-t');
    if (I18N[lang] && I18N[lang][k]) el.textContent = I18N[lang][k];
  });
  // data-i18n (text content) — alternative
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const k = el.getAttribute('data-i18n');
    if (I18N[lang] && I18N[lang][k]) el.textContent = I18N[lang][k];
  });
  // data-tp (placeholder)
  document.querySelectorAll('[data-tp]').forEach(el => {
    const k = el.getAttribute('data-tp');
    if (I18N[lang] && I18N[lang][k]) el.placeholder = I18N[lang][k];
  });
  // data-i18n-placeholder (placeholder) — alternative
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const k = el.getAttribute('data-i18n-placeholder');
    if (I18N[lang] && I18N[lang][k]) el.placeholder = I18N[lang][k];
  });
  saveLang();
}
function saveLang() {
  try {
    localStorage.setItem('goldbrix_lang', currentLang);
    localStorage.setItem('gbx_lang', currentLang);
  } catch(e) {}
}
function loadLang() {
  try {
    // FIX: read gbx_lang FIRST (V3 picker writes only this key)
    const l = localStorage.getItem('gbx_lang') ||
              localStorage.getItem('goldbrix_lang') ||
              localStorage.getItem('gbx-lang');
    if (l) currentLang = l;
  } catch(e) {}
}


